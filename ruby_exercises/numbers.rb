# Entero
x = 100
puts "x -> #{x}"
puts "x.class -> #{x.class}"

puts "x.odd? -> #{x.odd?}"         # .odds -> retorna true si es impar
puts "x.even? -> #{x.even?}"        # .even -> retorna true si es par

puts "x + 2 -> #{x + 2}"
puts "x - 2 -> #{x - 2}"
puts "x * 2 -> #{x * 2}"
puts "x / 2 -> #{x / 3}"
puts "x % 2 -> #{x % 2}"
puts "x ** 100 -> #{x ** 100}"   # -> ruby puede maneja numeros grandes sin problemas


